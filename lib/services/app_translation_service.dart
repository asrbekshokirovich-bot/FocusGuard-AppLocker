  static const String _langKey = 'selected_language';
  String _currentLanguage = 'uz';
  final ValueNotifier<String> languageNotifier = ValueNotifier<String>('uz');

  String get currentLanguage => _currentLanguage;

  Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    _currentLanguage = prefs.getString(_langKey) ?? 'uz';
    languageNotifier.value = _currentLanguage;
  }

  Future<void> setLanguage(String code) async {
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString(_langKey, code);
    _currentLanguage = code;
    languageNotifier.value = code;

    // Rejalashtirilgan notifikatsiyalarni qayta tuzish — chunki
    // ularning matni schedule paytidagi tilda Android tomonida
    // saqlanadi. Til o'zgargandan keyin eski tarjima bilan chiqib
    // qoladigan bug oldini olamiz:
    //   • 11:25 — StreakReminderService.scheduleDailyReminder()
    //   • 23:55 — TimerNotificationService.scheduleDailySummary()
    // Eski schedule avtomatik almashtiriladi (xuddi shu ID).
    try {
      await StreakReminderService().scheduleDailyReminder(hour: 11, minute: 25);
      await TimerNotificationService().scheduleDailySummary();
    } catch (_) {
      // Aksincha tilni o'zgartirishni bloklamaymiz
    }
  }

  TextStyle getFont({
    Color? color,
    double? fontSize,
    FontWeight? fontWeight,
    double? letterSpacing,
    double? height,
    TextDecoration? decoration,
  }) {
    if (_currentLanguage == 'ko') {
      return GoogleFonts.notoSansKr(
        color: color,
        fontSize: fontSize,
        fontWeight: fontWeight,
        letterSpacing: letterSpacing,
        height: height,
        decoration: decoration,
      );
    }
    return GoogleFonts.inter(
      color: color,
      fontSize: fontSize,
      fontWeight: fontWeight,
      letterSpacing: letterSpacing,
      height: height,
      decoration: decoration,
    );
  }

  String translate(String key) {
    List<String> keys = key.split('.');
    // Try current language
    dynamic value = _translations[_currentLanguage];

    if (value != null) {
      dynamic current = value;
      bool found = true;
      for (String k in keys) {
        if (current is Map && current.containsKey(k)) {
          current = current[k];
        } else {
          found = false;
          break;
        }
      }
      if (found && current is String) return current;
    }

    // Fallback to English
    return _getFallback(key);
  }

  List<dynamic> translateList(String key) {
    List<String> keys = key.split('.');
    dynamic value = _translations[_currentLanguage];

    if (value != null) {
      dynamic current = value;
      bool found = true;
      for (String k in keys) {
        if (current is Map && current.containsKey(k)) {
          current = current[k];
        } else {
          found = false;
          break;
        }
      }
      if (found && current is List) return current;
    }

    // Fallback to English
    dynamic fallback = _translations['en'];
    for (String k in keys) {
      if (fallback is Map && fallback.containsKey(k)) {
        fallback = fallback[k];
      } else {
        return [];
      }
    }
    return (fallback is List) ? fallback : [];
  }

  String _getFallback(String key) {
    List<String> keys = key.split('.');
    dynamic value = _translations['en'];

    for (String k in keys) {
      if (value is Map && value.containsKey(k)) {
        value = value[k];
      } else {
        return key;
      }
    }
    return (value is String) ? value : key;
  }

